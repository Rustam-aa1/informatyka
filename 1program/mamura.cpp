
#include <iostream>
using namespace std;

int main() {
    int liczba_calkowita = 42;
    float liczba_zmiennoprzecinkowa = 3.14f;
    char znak = 'A';
    bool wartosc_logiczna = true;

    cout << "Typ int: " << liczba_calkowita << endl;
    cout << "Typ float: " << liczba_zmiennoprzecinkowa << endl;
    cout << "Typ char: " << znak << endl;
    cout << "Typ bool: " << wartosc_logiczna << endl;

    return 0;
}